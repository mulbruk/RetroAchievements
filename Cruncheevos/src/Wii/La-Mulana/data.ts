export type Locale = 'NA' | 'EU' | 'JP';
export type FooBar = ((locale: Locale) => Condition.Value);

function address(opts: {en: Condition.Value, jp: Condition.Value}): FooBar {
  return (locale) => match(locale)
    .with('NA', () => opts.en)
    .with('EU', () => opts.en)
    .with('JP', () => opts.jp)
    .exhaustive();
}

